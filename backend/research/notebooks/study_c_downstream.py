# ── 2. PromtTriage Generation (Qwen3-397B MoE via Together) ──

def get_prompttriage_system(task: dict, target_model: str) -> str:
    """Generate the 'prompttriage' condition via Qwen 397B."""
    
    # Simulate the RAG retrieval that the UI would do
    # For this study, we're testing the generative power of the vertical AI
    # based on the downstream task descriptions
    
    meta_prompt = f"""You are PromptTriage, an expert system prompt engineer.
Create a highly optimized, production-ready SYSTEM PROMPT for the {target_model} model.

The user wants the assistant to perform this downstream task:
"{task['task']}"

Generate ONLY the system prompt text. Include:
1. Expert persona/role definition
2. Specific behavioral rules and constraints
3. Clear formatting instructions
4. Quality standards relevant to the task

DO NOT output markdown backticks (```). DO NOT output conversation. ONLY output the raw system prompt text."""

    headers = {
        "Authorization": f"Bearer {os.getenv('TOGETHER_API_KEY')}",
        "Content-Type": "application/json",
    }
    payload = {
        "model": "Qwen/Qwen3.5-397B-A17B",  # Use the big MoE!
        "max_tokens": 1500,
        "temperature": 0.2,
        "messages": [{"role": "user", "content": meta_prompt}],
    }
    
    resp = requests.post("https://api.together.xyz/v1/chat/completions", headers=headers, json=payload, timeout=60)
    resp.raise_for_status()
    pt_prompt = resp.json()["choices"][0]["message"]["content"].strip()
    
    # Clean markdown fences
    if pt_prompt.startswith('```'):
        pt_prompt = re.sub(r'^```(?:markdown|text)?\s*', '', pt_prompt)
        pt_prompt = re.sub(r'\s*```$', '', pt_prompt)
        
    return pt_prompt

# ── 3. Downstream Runners ──

def run_gemini(system_prompt: str, user_prompt: str) -> str:
    from google import genai
    client = genai.Client(api_key=os.getenv("GOOGLE_API_KEY"))
    config = {"temperature": 0.3, "max_output_tokens": 4000}
    if system_prompt:
        config["system_instruction"] = system_prompt
        
    try:
        resp = client.models.generate_content(
            model="gemini-3.1-pro-preview",
            contents=user_prompt,
            config=config
        )
        return resp.text
    except Exception as e:
        return f"[ERROR: {e}]"

def run_claude(system_prompt: str, user_prompt: str) -> str:
    from anthropic import AnthropicVertex
    # Fix for AnthropicVertex Initialization
    region = "us-east5"
    project_id = os.getenv("GOOGLE_CLOUD_PROJECT", "modelsandtraining")
    
    try:
        client = AnthropicVertex(project_id=project_id, region=region)
        kwargs = {
            "model": "claude-3-5-sonnet-v2@20241022", # Correct Vertex model name
            "max_tokens": 4000,
            "temperature": 0.3,
            "messages": [{"role": "user", "content": user_prompt}]
        }
        if system_prompt:
            kwargs["system"] = system_prompt
            
        resp = client.messages.create(**kwargs)
        return resp.content[0].text
    except Exception as e:
        return f"[ERROR: {e}]"

# ── 4. Llama 4 Maverick Judge ──

def get_vertex_token():
    creds, proj = google.auth.default(scopes=["https://www.googleapis.com/auth/cloud-platform"])
    creds.refresh(google.auth.transport.requests.Request())
    return creds.token

def judge_output(task_def: dict, generated_output: str, access_token: str) -> dict:
    
    rubric = f"""Score this AI-generated response on 5 dimensions (1-10 each).

DOWNSTREAM TASK:
"{task_def['task']}"

AI GENERATED OUTPUT:
---
{generated_output[:8000]}
---

Dimensions:
1. task_completion (1-10): Did it fully answer the prompt and satisfy all requirements?
2. domain_accuracy (1-10): Is the code/writing/analysis professionally accurate and optimal?
3. specificity (1-10): Are the details concrete, avoiding generic fluff or vagueness?
4. edge_cases (1-10): Did it proactively handle errors, edge cases, or nuanced tradeoffs?
5. format_structure (1-10): Is it highly readable, properly structured, and clean?

Respond with ONLY this exact JSON object:
{{"task_completion": N, "domain_accuracy": N, "specificity": N, "edge_cases": N, "format_structure": N}}"""

    url = "https://us-east5-aiplatform.googleapis.com/v1/projects/modelsandtraining/locations/us-east5/endpoints/openapi/chat/completions"
    headers = {"Authorization": f"Bearer {access_token}", "Content-Type": "application/json"}
    payload = {
        "model": "meta/llama-4-maverick-17b-128e-instruct-maas",
        "max_tokens": 100,
        "temperature": 0.0,
        "messages": [
            {"role": "system", "content": "You are a JSON-only scoring API. Respond ONLY with exactly 5 integer scores in JSON format."},
            {"role": "user", "content": rubric}
        ]
    }
    
    resp = requests.post(url, headers=headers, json=payload, timeout=60)
    resp.raise_for_status()
    raw = resp.json()["choices"][0]["message"]["content"].strip()
    
    if raw.startswith('```'):
        raw = re.sub(r'^```(?:json)?\s*', '', raw)
        raw = re.sub(r'\s*```$', '', raw)
        
    try:
        d = json.loads(raw)
        if all(k in d for k in ['task_completion', 'domain_accuracy', 'specificity', 'edge_cases', 'format_structure']):
            return d
    except:
        pass
    
    # Fallback regex
    m = re.search(r'\{[^{}]*"task_completion"[^{}]*\}', raw, re.DOTALL)
    if m:
        try:
            return json.loads(m.group())
        except:
            pass
            
    print(f"    [Judge Parse Fail] {raw[:50]}")
    return {"parse_error": raw[:100]}

